package org.imran.helloworld;

/***
 * Create a file named "HelloWorld.java" and within this file, create a public class named "HelloWorld"
 * In Java Programming Language you need to add "class" keyword for creating a class.
 * <p>
 * Every line of code that runs in Java must be inside a class. In our example, we named the class HelloWorld.
 * A class should always start with an uppercase first letter. Java is case-sensitive.
 * The name of the java file must match the class name.
 */
public class HelloWorld {

    // Add a public static method "main()". This is the entry point of a Java Program.
    // Any code inside the main() method will be executed.
    // When you run this program it will print "Hello World".
    // We use println() for printing text on the screen.
    public static void main(String[] args) {
        System.out.println("Hello World!");
    }

}
